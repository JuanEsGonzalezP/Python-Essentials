""" Programa que identifica el tipo de dato de un valor ingresado por el 
usuario, se realizarán cinco interacciones """

print("Programa de identificador de interacciones")

inter1=input("Primera Interacción, ingrese un valor cualquiera: ") #Ingreso de primera interacción"
print("Este tipo de dato en Python es:")
print(type(inter1))

inter2=input("Segunda Interacción, ingrese un valor cualquiera: ") #Ingreso de segunda interacción"
print("Este tipo de dato en Python es:")
print(type(inter2))

inter3=input("Tercera Interacción, ingrese un valor cualquiera: ") #Ingreso de tercera interacción"
print("Este tipo de dato en Python es:")
print(type(inter3))

inter4=input("Cuarta Interacción, ingrese un valor cualquiera: ") #Ingreso de cuarta interacción"
print("Este tipo de dato en Python es:")
print(type(inter4))

inter5=input("Quinta Interacción, ingrese un valor cualquiera: ") #Ingreso de quinta interacción"
print("Este tipo de dato en Python es:")
print(type(inter5))
print("¡YA NO SE HARÁN MÁS INTERACCIONES!")         #Indicador de fin de interacciones

